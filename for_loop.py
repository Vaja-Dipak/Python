'''
    It auto incremented loop
    Range bracket syntax:
        (starting_point, ending_point, Updation)

        
Syntax

    for  i(variable)  in  range(range>i):
        statement

'''

for i in range(10):
    print(i)
print("---------------")


for i in range(3,10):
    print(i)
print("---------------")


for i in range(0,15,3):
    print(i)
print("---------------")


for i in range(10,0,-1):
    print(i)